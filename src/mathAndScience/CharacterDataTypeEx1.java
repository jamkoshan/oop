package mathAndScience;

public class CharacterDataTypeEx1 {

	public static void main(String[] args) {
		char uppercase, lowercase;
		int i;
		uppercase = 'A';
		lowercase = 'a';
		
		for (i = 1; i <= 46; i++) {
			System.out.println(i + "\t" + uppercase + "\t" + lowercase);
			uppercase++;
			lowercase++;
			

		}
	}
 

}